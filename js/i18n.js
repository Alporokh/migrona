(function () {
  const LANGS = ["en", "et", "ru"];
  const STORE = "migrona-lang";
  const originals = {}; // captured English text per key

  function captureOriginals() {
    document.querySelectorAll("[data-i18n]").forEach((el) => {
      originals[el.getAttribute("data-i18n")] = el.textContent;
    });
  }

  function apply(lang) {
    if (!LANGS.includes(lang)) lang = "en";
    document.documentElement.setAttribute("lang", lang);
    const dict = lang === "en" ? null : I18N[lang];
    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      if (!dict) {
        if (key in originals) el.textContent = originals[key];
      } else if (key in dict) {
        el.textContent = dict[key];
      } else if (key in originals) {
        el.textContent = originals[key];
      }
    });
    document.querySelectorAll(".lang-btn").forEach((b) => {
      b.setAttribute("aria-pressed", String(b.dataset.lang === lang));
    });
    try { localStorage.setItem(STORE, lang); } catch (e) {}
  }

  function init() {
    captureOriginals();
    let lang = "en";
    try { lang = localStorage.getItem(STORE) || "en"; } catch (e) {}
    apply(lang);
    document.querySelectorAll(".lang-btn").forEach((b) => {
      b.addEventListener("click", () => apply(b.dataset.lang));
    });
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", init);
  } else {
    init();
  }

  window.MigronaI18n = { apply };
})();
